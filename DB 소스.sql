CREATE TABLE MEMBER (
    ID VARCHAR2(50) PRIMARY KEY,       
    PWD VARCHAR2(100),       
    NAME VARCHAR2(100) NOT NULL,       
    TEL VARCHAR2(150),                  
    ADDRESS VARCHAR2(150),             
    signupRoute VARCHAR2(150),  
    gender VARCHAR2(50),                    
    kakaoid VARCHAR2(50),              
    registration_date DATE,            
    mail VARCHAR2(100),                
    birth DATE
);

CREATE TABLE HOTEL_REVIEWS (
    REVIEW_ID NUMBER NOT NULL, 
    DEFAULT_NUM VARCHAR2(50 BYTE) NOT NULL, 
    USER_ID VARCHAR2(50 BYTE) NOT NULL, 
    RATING NUMBER(1,0) NOT NULL CHECK (RATING BETWEEN 1 AND 5), 
    REVIEW_TEXT CLOB NOT NULL, 
    REVIEW_DATE DATE DEFAULT SYSDATE NOT NULL, 
    IMAGE_PATH VARCHAR2(255 BYTE), 
    CONSTRAINT PK_HOTEL_REVIEWS PRIMARY KEY (REVIEW_ID),
    CONSTRAINT FK_REVIEWS_HOTEL FOREIGN KEY (DEFAULT_NUM) REFERENCES HOTEL_LIST (DEFAULT_NUM) ON DELETE CASCADE
);

CREATE TABLE HOTEL_RESERVATIONS(
    DEFAULT_NUM VARCHAR2(10 BYTE) NOT NULL, 
    RESERVATION_COUNT VARCHAR2(10 BYTE) CHECK (RESERVATION_COUNT >= 0)
    
);

INSERT INTO hotel_reservations (default_num, reservation_count)
