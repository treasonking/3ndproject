FROM hotel_list;


CREATE TABLE REFRESH_TOKENS (
    ID VARCHAR2(255 CHAR) PRIMARY KEY,
    EXPIRES_AT TIMESTAMP NOT NULL,
    ISSUED_AT TIMESTAMP NOT NULL,
    TOKEN VARCHAR2(512 CHAR) NOT NULL,
    USERNAME VARCHAR2(100 CHAR) NOT NULL,
    CONSTRAINT FK_REFRESH_TOKENS_USERNAME FOREIGN KEY (USERNAME) REFERENCES HR.MEMBER(ID)
);




CREATE TABLE FAVORITES(
    ID VARCHAR2(50),
    DEFAULT_NUM VARCHAR2(10),
    CONSTRAINT PK_FAVORITES PRIMARY KEY (ID, DEFAULT_NUM),
    CONSTRAINT FK_FAVORITES_MEMBER FOREIGN KEY (ID) REFERENCES MEMBER (ID) ON DELETE CASCADE,
    CONSTRAINT FK_FAVORITES_HOTEL FOREIGN KEY (DEFAULT_NUM) REFERENCES HOTEL_LIST (DEFAULT_NUM) ON DELETE CASCADE
);


CREATE TABLE RESERVATION(
    IMP_UID VARCHAR2(100) PRIMARY KEY, 
    PRODUCT_NAME VARCHAR2(100) NOT NULL,  -- NOT NULL 조건을 직접 열 정의에 포함
    COST NUMBER(30) NOT NULL, 
    EMAIL VARCHAR2(100) NOT NULL, 
    NAME VARCHAR2(100) NOT NULL, 
    TEL VARCHAR2(100) NOT NULL, 
    ADDRESS VARCHAR2(100) NOT NULL, 
    DATERANGE VARCHAR2(1000), 
    PAY_DATE DATE, 
    PERSON VARCHAR2(100), 
    DEFAULT_NUM VARCHAR2(10),
    CONSTRAINT FK_RESERVATION_DEFAULT_NUM
    FOREIGN KEY(DEFAULT_NUM)
    REFERENCES HOTEL_LIST(DEFAULT_NUM)
);
